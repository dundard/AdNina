// Helper function to get scaled forecast data
export function getScaledForecast(budget) {
    const baseBudget = 30;
    const scale = budget / baseBudget;
    
    return {
        labels: forecast.labels,
        seriesA: forecast.seriesA.map(val => Math.round(val * scale)),
        seriesB: forecast.seriesB.map(val => Math.round(val * scale))
    };
}

// Helper function to get estimated reach based on budget
export function getEstimatedReach(budget) {
    const baseBudget = 30;
    const baseReachMin = 976900;
    const baseReachMax = 1465400;
    const scale = budget / baseBudget;
    
    return {
        min: Math.round(baseReachMin * scale),
        max: Math.round(baseReachMax * scale)
    };
}

// Performance metrics generator
export function generatePerformanceMetrics(budget, duration = 14) {
    const dailyBudget = budget;
    const totalBudget = dailyBudget * duration;
    const scale = budget / 30; // base scale
    
    return {
        reach: {
            min: Math.round(976900 * scale),
            max: Math.round(1465400 * scale)
        },
        impressions: {
            min: Math.round(12500 * scale * duration),
            max: Math.round(18750 * scale * duration)
        },
        clicks: {
            min: Math.round(375 * scale * duration),
            max: Math.round(562 * scale * duration)
        },
        ctr: '3.0%',
        cpc: `$${(dailyBudget / (562 * scale)).toFixed(2)}`,
        conversions: {
            min: Math.round(15 * scale * duration),
            max: Math.round(28 * scale * duration)
        },
        conversionRate: '4.5%',
        costPerConversion: `$${(totalBudget / (28 * scale)).toFixed(2)}`
    };
}

